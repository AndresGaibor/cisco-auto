export type PTDeviceModel = Extract<keyof typeof allDeviceTypes, string>;
export type PTLinkType = keyof typeof allLinkTypes;
export type PTModuleType = keyof typeof allModuleTypes;

export function getPTDeviceType(model: string): number | undefined {
  return (allDeviceTypes as Record<string, number>)[model];
}

export function getPTLinkType(linkType: string): number | undefined {
  return (allLinkTypes as Record<string, number>)[linkType];
}

export function getPTModuleType(moduleType: string): number | undefined {
  return (allModuleTypes as Record<string, number>)[moduleType];
}

export function isPTDeviceModel(model: string): model is PTDeviceModel {
  return model in allDeviceTypes;
}

export function isPTLinkType(linkType: string): linkType is PTLinkType {
  return linkType in allLinkTypes;
}

export function isPTModuleType(moduleType: string): moduleType is PTModuleType {
  return moduleType in allModuleTypes;
}
